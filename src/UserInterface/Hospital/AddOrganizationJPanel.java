    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        int count = 0;
        if (txtOrganizationName.getText().trim().isEmpty()) {
            count++;
        }
        if (count > 0) {
            JOptionPane.showMessageDialog(null, "Please enter all the details", "Information", JOptionPane.ERROR_MESSAGE);
        } else {

            if (!txtOrganizationName.getText().trim().isEmpty()) {

                for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    int flag = 0;
                    String orgName = txtOrganizationName.getText();
                    if ((type.getValue()).equals("Hospital")) {
                        flag = 1;
                        break;

                    }
                    if (flag == 0) {
                        enterprise.getOrganizationDirectory().createOrganization(type, orgName);
                        populateTable();
                    } else {
                        JOptionPane.showMessageDialog(null, "Already existing");
                    }

                }
            }
        }
        populateTable();
         JOptionPane.showMessageDialog(null, "Organization already exists", "Information", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_btnCreateActionPerformed

    private void txtOrganizationNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtOrganizationNameKeyPressed
        // TODO add your handling code here:
        char percharacter = evt.getKeyChar();
        if (percharacter >= 65 && percharacter < 90 || percharacter >= 97 && percharacter <= 122 || percharacter == 32 || percharacter == 8) {
            lblStatus.setText("");
            txtOrganizationName.setEditable(true);
            txtOrganizationName.setBackground(Color.white);
        } else {
            evt.consume();
            lblStatus.setText("Organization name can only be alphabetic");
            txtOrganizationName.setEditable(false);
            txtOrganizationName.setBackground(Color.yellow);
        }

    }//GEN-LAST:event_txtOrganizationNameKeyPressed

