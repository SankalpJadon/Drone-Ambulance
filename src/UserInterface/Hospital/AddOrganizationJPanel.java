    private void populateCombo() {
        cmbOrganization.removeAllItems();
        String enterpriseType = enterprise.getEnterpriseType().getValue();
        for (Type type : Organization.Type.values()) {
            if (enterpriseType.equals("HospitalEnterprise")) {
                if (!(type.getValue().equals(Type.Mayor.getValue())) && !(type.getValue().equals(Type.SysAdmin.getValue())) && !(type.getValue().equals(Type.Doctor.getValue())) && !(type.getValue().equals(Type.DoctorAdmin.getValue())) && !(type.getValue().equals(Type.Drone.getValue())) && !(type.getValue().equals(Type.DroneWarehouseAdmin.getValue())) && !(type.getValue().equals(Type.Patient.getValue())) && !(type.getValue().equals(Type.SensorAdmin.getValue()))) {
                    cmbOrganization.addItem(type);
                }
            }
        }
    }

    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblOrganizations.getModel();
        dtm.setRowCount(0);
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof HospitalOrganization) {
                Object[] row = new Object[3];
                row[0] = org;
                row[1] = org.getClass();
                row[2] = org.getOrganizationId();

                dtm.addRow(row);
            }

        }
    }


    private void btnDeleteOrgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteOrgActionPerformed
        int selectedRow = tblOrganizations.getSelectedRow();
        if (selectedRow >= 0) {
            Organization org = (Organization) tblOrganizations.getValueAt(selectedRow, 0);
            enterprise.getOrganizationDirectory().deleteOrganization(org);
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select an entry from the table");
        }
    }//GEN-LAST:event_btnDeleteOrgActionPerformed

