    private void populateHospitalTable() {

        DefaultTableModel dtm = (DefaultTableModel) tblHospital.getModel();
        dtm.setRowCount(0);
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof HospitalOrganization) {
                for (Hospital hospital : org.getHospitalDirectory().getHospitalList()) {
                    Object[] row = new Object[4];
                    row[0] = hospital;
                    row[1] = hospital.getAddress();
                    row[2] = hospital.getHospitalID();
                    row[3] = hospital.getPhone();

                    dtm.addRow(row);

                }
            }
        }
    }


