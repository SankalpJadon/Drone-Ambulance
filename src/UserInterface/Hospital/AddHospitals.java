    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int count = 0;
        if (txtHospitalID.getText().trim().isEmpty()) {
            count++;
        }
        if (txtHospitalAddress.getText().trim().isEmpty()) {
            count++;
        }
        if (txtHospitalName.getText().trim().isEmpty()) {
            count++;
        }
        if (txtHospitalPhone.getText().trim().isEmpty()) {
            count++;
        }

        if (count > 0) {
            JOptionPane.showMessageDialog(null, "Please enter all the details", "Information", JOptionPane.ERROR_MESSAGE);
        }

        if ((txtHospitalPhone.getText().length() > 10)||(txtHospitalPhone.getText().length() < 10)) {
            JOptionPane.showMessageDialog(null, "Please enter only 10 digit phone number", "Information", JOptionPane.ERROR_MESSAGE);

        } else {

            getLocation(42.358544, -71.16394, 6000);
            for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (org instanceof HospitalOrganization) {
                    Hospital hospital = org.getHospitalDirectory().createHospital(txtHospitalName.getText(), txtHospitalAddress.getText(), Integer.parseInt(txtHospitalID.getText()), txtHospitalPhone.getText(), longitude, latitude);
                    populateHospitalTable();
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblHospital.getSelectedRow();
        if (selectedRow >= 0) {
            Hospital hospital = (Hospital) tblHospital.getValueAt(selectedRow, 0);

            for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (org instanceof HospitalOrganization) {
                    org.getHospitalDirectory().deleteHospital(hospital);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row from the table to be deleted", "Information", JOptionPane.ERROR_MESSAGE);

        }
        populateHospitalTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void txtHospitalNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtHospitalNameKeyPressed
        char percharacter = evt.getKeyChar();
        if (percharacter >= 65 && percharacter < 90 || percharacter >= 97 && percharacter <= 122 || percharacter == 32 || percharacter == 8) {
            lblName.setText("");
            txtHospitalName.setEditable(true);
            txtHospitalName.setBackground(Color.white);
        } else {
            evt.consume();
            lblName.setText("Hospital name can only be alphabetic");
            txtHospitalName.setEditable(false);
            txtHospitalName.setBackground(Color.yellow);
        }

    }//GEN-LAST:event_txtHospitalNameKeyPressed

