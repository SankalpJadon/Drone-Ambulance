    private void txtHospitalIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtHospitalIDKeyPressed
        int key = evt.getKeyCode();
        if ((key >= evt.VK_0 && key <= evt.VK_9) || (key >= evt.VK_NUMPAD0 && key <= evt.VK_NUMPAD9) || (key == evt.VK_BACK_SPACE)) {
            txtHospitalID.setEditable(true);
            txtHospitalID.setBackground(Color.white);
            lblID.setText("");
        } else {
            txtHospitalID.setEditable(false);
            txtHospitalID.setBackground(Color.yellow);
            lblID.setText("Please enter only numbers");
        }
    }//GEN-LAST:event_txtHospitalIDKeyPressed

